package OP;

public class Kho {
	String MaKho;
	String TenKho;
	
	public Kho(String maKho, String tenKho) {
		super();
		MaKho = maKho;
		TenKho = tenKho;
	}
	public String getMaKho() {
		return MaKho;
	}
	public void setMaKho(String maKho) {
		MaKho = maKho;
	}
	public String getTenKho() {
		return TenKho;
	}
	public void setTenKho(String tenKho) {
		TenKho = tenKho;
	}
	
}
