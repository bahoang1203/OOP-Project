package OP;

public class Kho {
	int MaKho;
	String TenKho;
	
	public Kho(int maKho, String tenKho) {
		super();
		MaKho = maKho;
		TenKho = tenKho;
	}
	public int getMaKho() {
		return MaKho;
	}
	public void setMaKho(int maKho) {
		MaKho = maKho;
	}
	public String getTenKho() {
		return TenKho;
	}
	public void setTenKho(String tenKho) {
		TenKho = tenKho;
	}
	
}
